package ch01_02;

public class CastingEx1 {
    public static void main(String[] args) {
        double d = 85.4;
        int score = (int) d;

        System.out.println("score = " + score);
        System.out.println("d = " + d);

        double s = 24.56;
        int a = (int) s;

        System.out.println("a = " + a);
        System.out.println("s = " + s);

    }
}
